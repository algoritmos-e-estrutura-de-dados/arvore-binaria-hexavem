class BinaryTree:
	def __init__(self):
		self.root = None

	def adicionar(self, node):
		if (self.root == None):
			self.root = node
			return

		target = self.root
		while True:
			if (node.value > target.value):
				if (target.direita is None):
					target.direita = node
					break

				target = target.direita

			else:
				if (target.esquerda is None):
					target.esquerda = node
					break

				target = target.esquerda

	def remover(self, node):
		target = self.root
		while True:
			if (node.value == target.value):
				target.value = None
				break                                                                                                         
                                        
